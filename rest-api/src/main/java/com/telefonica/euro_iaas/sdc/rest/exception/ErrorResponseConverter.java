package com.telefonica.euro_iaas.sdc.rest.exception;

import javax.xml.bind.annotation.XmlRootElement;

@XmlRootElement
public class ErrorResponseConverter {

    String message;
    Integer code;
    
    public ErrorResponseConverter() {
        super();
    }

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }

    public Integer getCode() {
        return code;
    }

    public void setCode(Integer code) {
        this.code = code;
    }

    public ErrorResponseConverter(Throwable cause) {

        ErrorCode errorCode = ErrorCode.find(cause.toString());
        this.code = errorCode.getCode();
        this.message = errorCode.getPublicMessage();

    }

}
